#include <bits/stdc++.h>
using namespace std;

int main()
{
    int A, B, i, j;
    cin >> A >> B;

    // ここにプログラムを追記
    i = 0;
    cout << "A:";
    while (i < A)
    {
        cout << "]";
        i++;
    }
    cout << endl;

    cout << "B:";
    while (j < B)
    {
        cout << "]";
        j++;
    }
    cout << endl;
}
